"""mcp-sdk-py — Model Context Protocol SDK for Python.

Wave 1 (S01): protocol foundations and conformance model.
Wave 2 (S02, S20, S43): JSON value model, common data types, lifecycle.
Wave 3 (S03, S21): JSON-RPC framing, content vocabulary.
Wave 4 (S04): result base, base params, error object, empty result.
Wave 5 (S05, S18, S19): _meta object, pagination, response caching.
Wave 6 (S06, S07, S17, S22, S23): stateless model, protocol revision,
  multi-round-trip requests, progress & cancellation, logging & trace context.
Wave 7 (S08, S12): discovery via server/discover, transport model & guarantees.
Wave 8 (S09, S10, S14): revision selection & negotiation errors, capability
  negotiation, Streamable HTTP request/headers/routing.
Wave 9 (S11, S13, S15, S16, S24, S26, S28, S30, S32, S33, S35): extensions map,
  stdio transport, HTTP responses & status mapping, streaming & subscriptions,
  tools, resources, prompts, elicitation, roots, sampling, authorization.
Wave 10 (S25, S27, S29, S31, S34, S36, S38): tools calling, resource reading,
  completion, elicitation form schema, error registry, OAuth flow, extensions.
Wave 11 (S37, S39, S41): OAuth registration, Tasks extension I, Interactive UI extension I.
Wave 12 (S40, S42, S45): Tasks extension II, Interactive UI extension II, conformance.
Wave 13 (S44, S46): security considerations, consolidated registries.
"""

